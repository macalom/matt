/**
 * @创建人：Administrator
 * @备注：两种方法-1.递归 2.迭代   3.itar 生成array for代码块
 */
class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
